CREATE FUNCTION XemDoanhThuTrongNgay()
RETURNS MONEY
AS
	BEGIN
		RETURN (
			SELECT SUM(SoTien) FROM DoanhThu
			WHERE DAY(ThoiGian) = DAY(GETDATE())
				AND MONTH(ThoiGian) = MONTH(GETDATE())
				AND YEAR(ThoiGian) = YEAR(GETDATE())
		)
	END
GO


