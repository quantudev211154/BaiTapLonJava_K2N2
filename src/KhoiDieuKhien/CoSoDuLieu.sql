CREATE VIEW XEMDANHSACHKHACHTRADIATRE
AS
	SELECT TOP 10 THE.MaKH
	FROM ThongTinPhieuThue T JOIN TheThanhVien THE ON T.MaThe = THE.MaThe
	WHERE T.NgayHetHan < GETDATE() AND MONTH(T.NgayLap) = MONTH(GETDATE()) AND YEAR(T.NgayLap) = YEAR(GETDATE())
	ORDER BY T.NgayHetHan DESC
GO


